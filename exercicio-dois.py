# 2) Faça um programa que receba quatro números e mostre a média entre os quatro.

numero_um = float(input('Digite o primeiro número: '))
numero_dois = float(input('Digite um segundo número: '))
numero_tres = float(input('Digite um terceiro número: '))
numero_quatro = float(input('Digite um quarto número: '))

media_dos_quarto_valores = (numero_um + numero_dois + numero_tres + numero_quatro) / 4

print(f'A média dos valores é: {media_dos_quarto_valores}')
