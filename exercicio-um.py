# 1) Faça um programa que receba dois números e mostre-os:
#     - A soma 
#     - A subtração
#     - A multiplicação
#     - E a divisão entre dois números

numero_um = float(input('Digite um número: '))
numero_dois = float(input('Digite outro número: '))

soma = numero_um + numero_dois
subtracao = numero_um - numero_dois
multiplicacao = numero_um * numero_dois
divisao = numero_um / numero_dois

print(f'A soma dos números é: {soma}')
print(f'A subtração dos números é: {subtracao}')
print(f'A multiplicação dos números é: {multiplicacao}')
print(f'A divisão dos números é: {divisao}')




